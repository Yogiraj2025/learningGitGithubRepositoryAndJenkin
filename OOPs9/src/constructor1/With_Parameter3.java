package constructor1;

public class With_Parameter3 
{ 
	int a;
	int b;
	public With_Parameter3()
	{
        a=10;
	    b=20;
	}
    public With_Parameter3(int c,int d)
	{
		a=c;
	    b=d;
	   
	}
	public void addition()
	{
		System.out.println(a+b);
	}
	public void multiplication()
	{
		System.out.println(a*b);
	}
	

}
